/** Function Implementation
-----------------------------------------------------------------------------*/
/* WIDTH:  16
 * POLY:   0x90D9
 * INIT:   0xFFFF
 * XOROUT: 0xFFFF
 * REFIN:  true
 * REFOUT: true
 *
 * Example: An algorithm that properly computes the CRC shall produce 0x4084
 * when itcomputes the CRC on the following test string of ASCll characters,
 * "123456789" with no null character.
 */

uint16 A825CRCInit(void)
{
    return 0xFFFF;
}

uint16 A825CRCCompute(uint16 CRC, const uint8 *pData, uint16 dataLen)
{
    for (uint16 i = 0; i < dataLen; ++i) {
        CRC = (uint16)((CRC << 8) ^ CRCTable0x90D9[((CRC >> 8) ^ (sRevertBit[pData[i]])) & 0xFF]);
    }

    return CRC;
}

uint16 A825CRCFinish(uint16 CRC)
{
    CRC = CRC ^ 0xFFFF;

    CRC = (uint16)(sRevertBit[(CRC >> 8) & 0xff] | (sRevertBit[CRC & 0xff] << 8));

    return CRC;
}

uint16 calcA825CRC(uint32 canId,  uint8 dataLen, const uint8 *pData)
{
    if (NULL == pData ||
        dataLen > 64  ||
        dataLen < 2) {
        return FALSE;
    }

    uint16 CRC = A825CRCInit();

    uint8 totalData[68];

    memset(totalData, 0, sizeof(totalData));

    totalData[0] = (canId >> 24) & 0x1f;

    for (uint16 i = 1; i < 4; ++i) {
        totalData[i] = (canId >> (24 - i * 8)) & 0xff;
    }

    for (uint16 i = 0; i < (dataLen - 2); ++i) {
        totalData[i + 4] = pData[i];
    }

    CRC = A825CRCCompute(CRC, totalData, dataLen - 2 + 4);

    return A825CRCFinish(CRC);
}

_Bool checkA825CRC(uint32 canId, uint8 dataLen, const uint8 *pData)
{
    if (NULL == pData ||
        dataLen > 64  ||
        dataLen < 2) {
        return FALSE;
    }

    uint16 calcCRC = calcA825CRC(canId,  dataLen, pData);
    uint16 rcvCRC = 0;
    rcvCRC |= (uint16)pData[dataLen - 1];
    rcvCRC |= (((uint16)pData[dataLen - 2]) << 8);

    if (calcCRC != rcvCRC) {
        return FALSE;
    }

    return TRUE;
}

void setA825CRC(uint32 canId, uint8 dataLen, uint8 *pData)
{
    if (NULL == pData ||
        dataLen > 64  ||
        dataLen < 2) {
        return;
    }

    uint16 CRC = calcA825CRC(canId, dataLen, pData);

    pData[dataLen - 2] = (uint8)(CRC >> 8);
    pData[dataLen - 1] = (uint8)(CRC);
}

